def main():
    base_dir = Path(__file__).resolve().parents[1]
    raw_path = base_dir / "data" / "udon_raw.json"
    old_path = base_dir / "data" / "udon.json"
    output_path = base_dir / "data" / "udon.json"

    with open(raw_path, 'r', encoding='utf-8') as f:
        stores = json.load(f)

    # 旧 udon.json から座標マップを構築（IDベース）
    old_coord = {}
    if old_path.exists():
        with open(old_path, 'r', encoding='utf-8') as f:
            old_data = json.load(f)
        for r in old_data:
            sid = extract_store_id(r.get('url', ''))
            if sid and r.get('lat') and r.get('lng'):
                old_coord[sid] = (r['lat'], r['lng'])
        print(f"旧 udon.json から {len(old_coord)} 件の座標をロード")

    print(f"対象: {len(stores)} 店舗")

    reused = geocoded = fallback = failed = 0

    for i, r in enumerate(stores):
        sid = extract_store_id(r.get('url', ''))
        pref = r.get('prefecture', '')
        area = r.get('area', '')

        # 1. 旧データから座標を再利用
        if sid and sid in old_coord:
            r['lat'], r['lng'] = old_coord[sid]
            reused += 1
            continue

        # 2. STATION_FALLBACK辞書
        if area in STATION_FALLBACK:
            r['lat'], r['lng'] = STATION_FALLBACK[area]
            print(f"  [{i+1}] ✅ FALLBACK: {r['name']} → {STATION_FALLBACK[area]}")
            geocoded += 1
            continue

        # 3. Nominatim API
        queries = [
            f"{r['name']} {area} {pref} うどん",
            f"{r['name']} {pref}",
            f"{area} {pref}",
        ]
        found = False
        for q in queries:
            time.sleep(1.1)
            lat, lng = geocode_nominatim(q)
            if lat and in_pref(lat, lng, pref):
                r['lat'], r['lng'] = lat, lng
                print(f"  [{i+1}] ✅ Nominatim: {r['name']} ({pref}) → ({lat:.4f}, {lng:.4f})")
                geocoded += 1
                found = True
                break

        if not found:
            # 4. 都道府県デフォルト
            if pref in PREF_DEFAULT:
                blat, blng = PREF_DEFAULT[pref]
                r['lat'] = round(blat + random.uniform(-0.015, 0.015), 6)
                r['lng'] = round(blng + random.uniform(-0.015, 0.015), 6)
                print(f"  [{i+1}] ⚠ DEFAULT: {r['name']} ({pref})")
                fallback += 1
            else:
                print(f"  [{i+1}] ❌ 失敗: {r['name']} ({pref})")
                failed += 1

    print(f"\n=== ジオコーディング結果 ===")
    print(f"  座標再利用: {reused} 件")
    print(f"  新規取得:   {geocoded} 件")
    print(f"  デフォルト: {fallback} 件")
    print(f"  失敗:       {failed} 件")
    print(f"  合計:       {len(stores)} 件")

    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(stores, f, ensure_ascii=False, indent=2)
    print(f"\n✅ 出力完了: {output_path} ({len(stores)} 店舗)")


if __name__ == "__main__":
    main()
