def extract_store_id(url):
    m = re.search(r'/(\d+)/?$', url)
    return m.group(1) if m else None


def geocode_nominatim(query, retries=2):
    encoded = urllib.parse.quote(query)
    url = f"https://nominatim.openstreetmap.org/search?q={encoded}&format=json&limit=1&countrycodes=jp"
    headers = {'User-Agent': 'UdonMapProject/2.0 (personal use)'}
    for attempt in range(retries):
        try:
            req = urllib.request.Request(url, headers=headers)
            with urllib.request.urlopen(req, timeout=10) as r:
                data = json.loads(r.read().decode())
                if data:
                    return float(data[0]['lat']), float(data[0]['lon'])
        except Exception as e:
            if attempt < retries - 1:
                time.sleep(2)
            else:
                print(f"    ⚠ Nominatim エラー: {query} - {e}")
    return None, None


