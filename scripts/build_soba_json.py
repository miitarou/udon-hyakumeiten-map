YEARS_WITH_REGION = [
    (2018, YEAR_2018, None),       # None = prefecture から推定
    (2021, YEAR_2021, None),
    (2022, YEAR_2022, None),
    (2024, YEAR_2024_EAST, 'EAST'),
    (2024, YEAR_2024_WEST, 'WEST'),
    (2025, YEAR_2025_EAST, 'EAST'),
    (2025, YEAR_2025_WEST, 'WEST'),
]


def build_soba_json():
    # URL -> entry の辞書（マージ用）
    store_map = {}

    for year, store_list, fixed_region in YEARS_WITH_REGION:
        for name, prefecture, area, url in store_list:
            region = fixed_region if fixed_region else infer_region(prefecture)

            if url in store_map:
                entry = store_map[url]
                if year not in entry['years']:
                    entry['years'].append(year)
                # 同じ店の region を 2024 以降のデータで上書き優先
                if fixed_region:
                    entry['region'] = fixed_region
            else:
                store_map[url] = {
                    "name": name,
                    "category": "soba",
                    "region": region,
                    "prefecture": prefecture,
                    "area": area,
                    "holiday": None,
                    "url": url,
                    "lat": None,
                    "lng": None,
                    "closed": False,
                    "firstSelected": False,
                    "years": [year],
                    "address": None,
                }

    # years を昇順に並べて firstSelected を決定
    all_years_seen = {}  # url -> min_year
    for url, entry in store_map.items():
        entry['years'].sort()
        all_years_seen[url] = min(entry['years'])

    # firstSelected: 最初に選出された年が最も新しいグループ（2025）の店は初選出候補
    # → 2025 だけに登場し、かつ 2024 以降のデータにしか登場しない店を初選出とする
    # 簡易判定: 2023 より前の年度リストにない = firstSelected
    early_urls = set()
    for year, store_list, _ in YEARS_WITH_REGION:
        if year <= 2022:
            for _, _, _, url in store_list:
                early_urls.add(url)

    for url, entry in store_map.items():
        if url not in early_urls:
            entry['firstSelected'] = True

    result = list(store_map.values())

    # prefecture 順でソート
    result.sort(key=lambda r: (r['prefecture'], r['name']))

    return result


def main():
    base_dir = Path(__file__).resolve().parents[1]
    output_path = base_dir / "data" / "soba_raw.json"

    stores = build_soba_json()

    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(stores, f, ensure_ascii=False, indent=2)

    east = sum(1 for s in stores if s['region'] == 'EAST')
    west = sum(1 for s in stores if s['region'] == 'WEST')
    print(f"✅ soba_raw.json 生成完了: {len(stores)} 店舗 (EAST: {east}, WEST: {west})")

    # 年度別集計
    year_count = {}
    for s in stores:
        for y in s['years']:
            year_count[y] = year_count.get(y, 0) + 1
    for y in sorted(year_count):
        print(f"  {y}: {year_count[y]} 店")


if __name__ == "__main__":
    main()
