def main():
    # プロジェクトルートからの相対パスで入出力ファイルを指定
    base_dir = Path(__file__).resolve().parents[1]
    input_path = base_dir / "data" / "restaurants_raw.json"
    output_path = base_dir / "data" / "restaurants.json"
    
    with open(input_path, 'r', encoding='utf-8') as f:
        restaurants = json.load(f)
    
    print(f"入力: {len(restaurants)} 店舗")
    
    # 1. ゴミデータの除去
    restaurants = [r for r in restaurants if r['name'] != '食べログ]']
    print(f"クレンジング後: {len(restaurants)} 店舗")
    
    # 2. 不足店舗の手動追加
    # EAST: 谷や（最初のエントリが壊れていたので手動追加）
    missing_east = [
        {
            "name": "谷や",
            "region": "EAST",
            "prefecture": "東京都",
            "area": "水天宮前駅",
            "holiday": "不定休",
            "url": "https://tabelog.com/tokyo/A1302/A130204/13114085/",
            "lat": 35.6840,
            "lng": 139.7849,
            "closed": False,
            "firstSelected": False
        }
    ]
    
    # 既存データに無いものだけ追加
    existing_urls = {r['url'] for r in restaurants}
    for r in missing_east:
        if r['url'] not in existing_urls:
            restaurants.insert(0, r)
            print(f"  追加: {r['name']} ({r['region']})")
    
    print(f"補完後: {len(restaurants)} 店舗")
    
    # EAST/WESTの数を確認
    east_count = sum(1 for r in restaurants if r['region'] == 'EAST')
    west_count = sum(1 for r in restaurants if r['region'] == 'WEST')
    print(f"  EAST: {east_count} 店, WEST: {west_count} 店")
    
    # 3. ジオコーディング
    geocoded = 0
    failed = 0
    skipped = 0
    
    for i, r in enumerate(restaurants):
        if r['lat'] is not None and r['lng'] is not None:
            skipped += 1
            continue
        
        area = r['area']
        prefecture = r['prefecture']
        
        # まずフォールバック辞書を確認
        if area in STATION_FALLBACK:
            r['lat'], r['lng'] = STATION_FALLBACK[area]
            geocoded += 1
            print(f"  [{i+1}/{len(restaurants)}] ✅ {r['name']} → フォールバック辞書 ({r['lat']}, {r['lng']})")
            continue
        
        # Nominatim APIで検索
        query = f"{area}, {prefecture}, Japan"
        time.sleep(1.1)  # レート制限: 1リクエスト/秒
        lat, lng = geocode_nominatim(query)
        
        if lat is not None:
            r['lat'] = lat
            r['lng'] = lng
            geocoded += 1
            print(f"  [{i+1}/{len(restaurants)}] ✅ {r['name']} → Nominatim ({lat}, {lng})")
        else:
            # 都道府県のデフォルト座標にフォールバック
            if prefecture in PREF_DEFAULT:
                r['lat'], r['lng'] = PREF_DEFAULT[prefecture]
                # 少しランダムにずらす（重なり防止）
                import random
                r['lat'] += random.uniform(-0.01, 0.01)
                r['lng'] += random.uniform(-0.01, 0.01)
                print(f"  [{i+1}/{len(restaurants)}] ⚠ {r['name']} → 都道府県デフォルト ({r['lat']:.4f}, {r['lng']:.4f})")
                geocoded += 1
            else:
                failed += 1
                print(f"  [{i+1}/{len(restaurants)}] ❌ {r['name']} → 失敗")
    
    print(f"\n=== ジオコーディング結果 ===")
    print(f"  成功: {geocoded}, スキップ: {skipped}, 失敗: {failed}")
    
    # 4. 出力
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(restaurants, f, ensure_ascii=False, indent=2)
    
    print(f"\n出力完了: {output_path}")
    print(f"合計: {len(restaurants)} 店舗")

if __name__ == "__main__":
    main()
