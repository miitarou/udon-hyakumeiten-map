# 年度・カテゴリ別の処理リスト
# (year, list, region_hint)
# region_hint: 'KAGAWA'/'EAST'/'WEST'/None (Noneは prefecture から推定)
YEAR_LISTS = [
    (2017, lambda: YEAR_2017,        None),
    (2018, lambda: YEAR_2018,        None),
    (2019, lambda: YEAR_2019,        None),
    (2020, lambda: YEAR_2020_TOKYO,  'EAST'),  # TOKYOはEASTに統合
    (2020, lambda: YEAR_2020_EAST,   'EAST'),
    (2020, lambda: YEAR_2020_WEST,   'WEST'),
    (2022, lambda: YEAR_2022_EAST,   'EAST'),
    (2022, lambda: YEAR_2022_WEST,   'WEST'),
    (2024, lambda: YEAR_2024_EAST,   'EAST'),
    (2024, lambda: YEAR_2024_WEST,   'WEST'),
    (2024, lambda: YEAR_2024_KAGAWA, 'KAGAWA'),
]


def build_udon_json():
    # store_id -> {正規URL, 各年度・region情報, prefecture, area, name}
    store_map = {}
    # store_id -> 信頼できる正規URL (2017以外で見つけたもの)
    canonical_url = {}

    # まず全年度を走査して正規URLマップを作る
    for year, list_fn, hint in YEAR_LISTS:
        if year == 2017:
            continue
        for name, pref, area, url in list_fn():
            sid = extract_store_id(url)
            if sid and sid not in canonical_url and is_valid_url(url, pref):
                canonical_url[sid] = url

    # 全年度のデータをマージ
    for year, list_fn, hint in YEAR_LISTS:
        for name, pref, area, url in list_fn():
            sid = extract_store_id(url)
            if not sid:
                print(f"⚠ store_id 取得失敗: {name} ({url})")
                continue

            # URL: 正規URL → なければ自前の URL を使う
            final_url = canonical_url.get(sid)
            if not final_url:
                if year == 2017 and is_valid_url(url, pref):
                    final_url = url
                elif is_valid_url(url, pref):
                    final_url = url
                else:
                    final_url = synthesize_url(pref, sid)

            if sid not in store_map:
                store_map[sid] = {
                    'name': name,
                    'category': 'udon',
                    'region': None,  # 後で確定
                    'prefecture': pref,
                    'area': area,
                    'holiday': None,
                    'url': final_url,
                    'lat': None,
                    'lng': None,
                    'closed': False,
                    'firstSelected': False,
                    'years': [],
                    'address': None,
                    '_region_hints': [],
                }
            entry = store_map[sid]
            if year not in entry['years']:
                entry['years'].append(year)
            if hint:
                entry['_region_hints'].append((year, hint))
            # nameやprefectureが新しい年度のものに更新（最新優先）
            if year >= max(entry['years']) - 1:
                entry['name'] = name
                # 新しいデータでprefecture/areaを上書き（より新しい店舗情報を優先）
                if pref and pref != entry['prefecture']:
                    pass  # prefecture変わるケースは少ないのでログだけ
                if area:
                    entry['area'] = area

    # region 確定
    for sid, entry in store_map.items():
        # 1. KAGAWA hint があればKAGAWA
        if any(h == 'KAGAWA' for _, h in entry['_region_hints']):
            entry['region'] = 'KAGAWA'
        # 2. 香川県 → KAGAWA (公式の独立判断に追従)
        elif entry['prefecture'] == '香川県':
            entry['region'] = 'KAGAWA'
        else:
            # 3. EAST/WEST hint の最新を採用
            hints = entry['_region_hints']
            if hints:
                # 年度降順でソートして最新のhintを使う
                hints.sort(key=lambda x: -x[0])
                entry['region'] = hints[0][1]
            else:
                # 4. 全国カテゴリしか登場しない → prefecture で推定
                entry['region'] = 'EAST' if entry['prefecture'] in EAST_PREFS else 'WEST'

        del entry['_region_hints']

        # firstSelected: 最初に出現した年が直近2回（2024/2022）以降だけのケース
        # → 過去年(2017-2020)に出現していない店舗が「初選出」
        early_years = [y for y in entry['years'] if y <= 2020]
        if not early_years and entry['years']:
            entry['firstSelected'] = True

        entry['years'].sort()

    result = list(store_map.values())
    result.sort(key=lambda r: (r['region'], r['prefecture'], r['name']))
    return result


def main():
    base_dir = Path(__file__).resolve().parents[1]
    output_path = base_dir / 'data' / 'udon_raw.json'

    stores = build_udon_json()

    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(stores, f, ensure_ascii=False, indent=2)

    east   = sum(1 for s in stores if s['region'] == 'EAST')
    west   = sum(1 for s in stores if s['region'] == 'WEST')
    kagawa = sum(1 for s in stores if s['region'] == 'KAGAWA')
    print(f"✅ udon_raw.json 生成完了: {len(stores)} 店舗")
    print(f"   EAST: {east}, WEST: {west}, KAGAWA: {kagawa}")

    from collections import Counter
    yc = Counter()
    for s in stores:
        for y in s['years']:
            yc[y] += 1
    for y in sorted(yc):
        print(f"   {y}: {yc[y]} 店")

    # 検証: 各年度の合計件数確認
    expected = {
        2017: 100, 2018: 100, 2019: 100,
        2020: 300, 2022: 200, 2024: 300,
    }
    print("\n=== 件数検証 ===")
    for y, exp in expected.items():
        actual = yc.get(y, 0)
        ok = '✅' if actual == exp else '⚠'
        print(f"   {ok} {y}: {actual} / {exp}")


if __name__ == '__main__':
    main()
