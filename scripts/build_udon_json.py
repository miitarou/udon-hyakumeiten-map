def extract_store_id(url):
    """食べログURL末尾の店舗IDを抽出。例: .../37000051/ → '37000051'"""
    m = re.search(r'/(\d+)/?$', url)
    return m.group(1) if m else None


def is_valid_url(url, prefecture):
    """URLが正しい都道府県スラグを含んでいるか判定 (2017データ判別用)"""
    if not url or 'tabelog.com' not in url:
        return False
    slug = PREF_SLUG.get(prefecture, '')
    if not slug:
        return False
    return f'/{slug}/' in url


def synthesize_url(prefecture, store_id):
    """2017のみの店舗で正規URLが手に入らない場合の簡易URL生成"""
    slug = PREF_SLUG.get(prefecture, '')
    if not slug or not store_id:
        return f'https://tabelog.com/'
    return f'https://tabelog.com/{slug}/{store_id}/'


